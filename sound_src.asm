; Input: A, descritor index
; Input HL: pointer to the state structure. It can be SOUND_BUFFER_1 or SOUND_BUFFER_2
;
; It does the following:
; 1. constructs BC = 0xB4xx using A,
; 2. reads bytes from that table,
; 3. checks if the sound can be added (CAN_ADD_SOUND),
; 4. if the channel was already occupied, compares priorities:
;   and 0f0h
;   cp (hl)
; 5. if the new priority is lower, aborts,
; 6. otherwise, fills several fields of the structure.
; The first byte of the descriptor contains:
;   - priority in the high nibble
;   - some type or subchannel in the low nibble
; And then come:
;   - pointers to the sequence,
;   - timers,
;   - initial parameters.

; HL = SOUND_BUFFER_1 or SOUND_BUFFER_2
QUEUE_SOUND_DESCRIPTOR:
    ; BC = 0xb4.. , with A
    
    ; OBtain the pointer BC
    ; B is fixed=0xB4 and C comes from A
    ld b, (TBL_SOUND_PARAMS & 0xFF00) >> 8; ;b51d	06 b4
	ld c,a			;b51f	4f
    
    ; Once the pointer has been obtained, it decodes the
    ; corresponding 5-byte block into memory (SOUND_BUFFER_1 or SOUND_BUFFER_2).
    ;
    ; If we call B0 B1 B2 B3 B4 the input sequence, it'll write into
    ; SOUND_BUFFER_1 (or 2) these 10 bytes:
    ; 01, B0 & 0xF0, B1, B0 & 0x0F, B2 & 0x0F, <BC pointer>, B3, B4, 01
    
    ; The decoded sequence will be processes by ADVANCE_SOUND_STREAM_IF_READY

    ; Read byte #0 in C
    ld a,(bc)		;b520	0a
    ld c,a			;b521	4f
    
	; Check if we can add a sound now
    ld a,(CAN_ADD_SOUND)	;b522	3a c2 e5
	and a			;b525	a7
	ret z			;b526	c8

	di			    ;b527	f3

    ; Write byte #0 (always a 1)
    ; This 1 means that it's active.
    ld a,(hl)		;b528	7e          A <-- (hl)
	ld (hl), 1		;b529	36 01       (hl) <-- 1
    inc hl			;b52b	23          hl++

	and a			;b52c	a7
	jr z,lb534h		;b52d	28 05       Jump if A == 0
	
    ; A != 0
    ; Compare priority.
    ld a,(bc)		;b52f	0a
	and 0f0h		;b530	e6 f0       A <-- (BC) & 0xf0
	cp (hl)			;b532	be          Compare (BC) & 0xf0 with (hl)
	ret c			;b533	d8          Return if (BC) & 0xf0 < (hl)
    ; Priority OK: go on
lb534h:

    ; Read in DE (2 bytes)

    ; Read byte #0 in D
	ld a,(bc)		;b534	0a
	and 00fh		;b535	e6 0f
	ld d,a			;b537	57
	inc bc			;b538	03

    ; Read byte #1 in E
	ld a,(bc)		;b539	0a
	ld e,a			;b53a	5f
	inc bc			;b53b	03

    ; Write (HL) <-- A, E, D
    ; Read byte #2 in A
	ld a,(bc)		;b53c	0a
	and 0f0h		;b53d	e6 f0
    ; Write byte #1
	ld (hl),a		;b53f	77
	inc hl			;b540	23

    ; Write E
    ; Write byte #2
	ld (hl),e		;b541	73
	inc hl			;b542	23

    ; Write D
    ; Write byte #3
	ld (hl),d		;b543	72
	inc hl			;b544	23
lb545h:
    ; Read byte #2 in A
	ld a,(bc)		;b545	0a
	and 00fh		;b546	e6 0f
    
    ; Write (HL) <-- A, C, B 
    ; Write byte #4
	ld (hl),a		;b548	77
	inc hl			;b549	23

    ; Bytes #5 and #6 are the BC pointer itself
    
    ; Write byte #5
	ld (hl),c		;b54a	71
	inc hl			;b54b	23

    ; Write byte #6
	ld (hl),b		;b54c	70
lb54dh:
	inc bc			;b54d	03

    ; Read byte #3  in A
	ld a,(bc)		;b54e	0a
	inc hl			;b54f	23
    
    ; Write byte #7 (HL) <-- A
	ld (hl),a		;b550	77
	inc bc			;b551	03

    ; Read byte #4 in A
	ld a,(bc)		;b552	0a
	inc hl			;b553	23
    
    ; Write byte #8 (HL) <-- A
	ld (hl),a		;b554	77
	inc hl			;b555	23
	
