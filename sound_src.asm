    ; SOUND_NUMBER > 128
	cp 192		    ;b4fb	fe c0
	jr nc,sound_more_eq_192	;b4fd	30 04   Jump if SOUND_NUMBER >= 192

    ; SOUND_NUMBER < 192
	add a, 144		;b4ff	c6 90
	jr lb512h		;b501	18 0f

