	call UPDATE_PERIOD_EFFECT		;b605	cd 7b b7 	. { . 
	ld (SOUNDS_UNKNOWN_TYPE),bc		;b608	ed 43 c4 e5 	. C . . 
	ld hl,0e5e1h		;b60c	21 e1 e5 	! . . 
	ld d,010h		;b60f	16 10 	. . 
