	call UPDATE_PERIOD_EFFECT		;b627	cd 7b b7 	. { . 
	ld (0e5c6h),bc		;b62a	ed 43 c6 e5 	. C . . 
	ld hl,0e5f7h		;b62e	21 f7 e5 	! . . 
	ld d,040h		;b631	16 40 	. @ 
